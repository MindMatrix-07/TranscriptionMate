type MatchRule = {
  clue: string;
  pattern: RegExp;
  weight: number;
};

type SourceProfile = {
  domain: string;
  name: string;
  rules: MatchRule[];
};

type Evidence = {
  clue: string;
  excerpt: string;
  weight: number;
};

export type SourceCandidate = {
  domain: string;
  evidence: Evidence[];
  name: string;
  score: number;
};

export type SourceFlag = {
  label: string;
  severity: "low" | "medium" | "high";
};

export type SourceReport = {
  candidates: SourceCandidate[];
  flags: SourceFlag[];
  summary: string;
  suspicion: "low" | "medium" | "high";
};

