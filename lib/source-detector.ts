function collectEvidence(text: string, rules: MatchRule[]) {
  const evidence: Evidence[] = [];

  for (const rule of rules) {
    const match = text.match(rule.pattern);

    if (!match || !match[0]) {
      continue;
    }

    evidence.push({
      clue: rule.clue,
      excerpt: match[0].trim(),
      weight: rule.weight,
    });
  }

  return evidence;
}

function getSuspicionScore(flags: SourceFlag[], candidates: SourceCandidate[]) {
  const directSiteHit = candidates.some((candidate) => candidate.score >= 10);
  const strongSiteHit = candidates.some((candidate) => candidate.score >= 18);
  const highFlags = flags.filter((flag) => flag.severity === "high").length;
  const mediumFlags = flags.filter((flag) => flag.severity === "medium").length;

  if (strongSiteHit || highFlags >= 2) {
    return "high" as const;
  }

  if (directSiteHit || highFlags >= 1 || mediumFlags >= 2) {
    return "medium" as const;
  }

  return "low" as const;
}

export function detectSource(rawLyrics: string): SourceReport {
  const text = rawLyrics.replace(/\r\n?/g, "\n").trim();

  if (!text) {
    return {
      candidates: [],
      flags: [],
      summary: "Paste lyrics first to run a source check.",
      suspicion: "low",
    };
  }

  const candidates = sourceProfiles
    .map((profile) => {
      const evidence = collectEvidence(text, profile.rules);
      const score = evidence.reduce((total, item) => total + item.weight, 0);

      return {
        domain: profile.domain,
        evidence,
        name: profile.name,
        score,
      };
    })
    .filter((candidate) => candidate.score > 0)
    .sort((left, right) => right.score - left.score)
    .slice(0, 3);

  const flags = collectEvidence(text, genericFlags).map((item) => ({
    label: item.clue,
    severity:
      item.weight >= 7 ? ("high" as const) : item.weight >= 5 ? ("medium" as const) : ("low" as const),
  }));

  const suspicion = getSuspicionScore(flags, candidates);

  let summary = "No strong external-source fingerprints were detected.";

  if (candidates.length > 0) {
    summary = `Likely external-source match: ${candidates[0].name}. Review the clues before accepting the lyrics.`;
  } else if (flags.length > 0) {
    summary =
      "The text shows copy-paste warning signs, but there is not enough fingerprint data to name a specific site.";
  }

  return {
    candidates,
    flags,
    summary,
    suspicion,
  };
}
